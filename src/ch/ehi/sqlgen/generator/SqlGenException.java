package ch.ehi.sqlgen.generator;

public class SqlGenException extends java.lang.Exception {

	public SqlGenException() {
		super();
	}

	public SqlGenException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SqlGenException(String arg0) {
		super(arg0);
	}

	public SqlGenException(Throwable arg0) {
		super(arg0);
	}

}
